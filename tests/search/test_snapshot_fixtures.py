"""Snapshot tests that replay captured live responses through the parser.

These tests give us deterministic, offline coverage of the response-side
parsing pipeline (no Google requests). Captured responses live under
``tests/search/fixtures/`` and are re-generated by running
``uv run python scripts/capture_fixtures.py``.

## Drift policy

These fixtures are real Google Flights responses, so they age out as
prices move, carriers add/drop routes, or Google reshapes the payload.
The assertions in this file are written to be *durable* against that
drift — they target filter enforcement (e.g. "no Delta when DL is
excluded") and parser structure (CO₂ populated on at least one row),
not specific prices or flight numbers.

If a snapshot test starts failing:

1. Re-run the live integration tests in
   ``test_search_flights_new_filters_live.py``. They issue the same
   queries against Google's API; if those pass and the snapshot fails,
   the fixture is stale.
2. Re-capture: ``uv run python scripts/capture_fixtures.py``.
3. Re-run the snapshot suite to confirm green.

If the live tests *also* fail with the same shape, Google has changed
the response and the parser needs updating — start with
``.reverse-eng/notes/response_map.md`` for position references.

Route-specific notes:
- ``flight_search_jfk_lax_oneway_usd`` — pure positional sanity checks
  (parser populates basic fields).
- ``flight_search_jfk_fra_oneworld`` — alliance filter enforcement.
- ``flight_search_buf_ath_min_layover_120`` — multi-stop trip; layover
  derivation + min-duration enforcement.
- ``flight_search_jfk_lax_exclude_dl`` — airline exclude enforcement.
- ``flight_search_jfk_lax_eur`` — currency URL param effective.
- ``flight_search_lax_lhr_rt_biz_2a1c`` — premium-cabin round-trip with
  family pax (2 adults + 1 child). Pinned regression for issue #165:
  Google emits these rows with an empty price head, and the parser must
  surface them with ``price=None`` rather than dropping the entire
  result set as malformed.
"""

from __future__ import annotations

import json
from pathlib import Path

import pytest

from fli.search._decoders import parse_flight_row

FIXTURE_DIR = Path(__file__).parent / "fixtures"


def _replay(name: str) -> list:
    """Replay a captured GetShoppingResults body through the row-level parser.

    Mirrors the exact path used by :meth:`SearchFlights.search` so the
    snapshot exercises the same decoder code in production.
    """
    body = (FIXTURE_DIR / name).read_text()
    # Same stripping logic as SearchFlights.search:
    outer = json.loads(body.lstrip(")]}'"))
    inner_str = outer[0][2]
    if not inner_str:
        return []
    inner = json.loads(inner_str)
    rows = [item for i in (2, 3) if isinstance(inner[i], list) for item in inner[i][0]]
    flights = []
    for row in rows:
        try:
            flights.append(parse_flight_row(row))
        except (AttributeError, KeyError, ValueError, TypeError):
            continue
    return flights


