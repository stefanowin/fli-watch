class TestSnapshotFlightShape:
    """Sanity-check that parsed flights have the expected fields populated."""

    def test_basic_fields_present(self, jfk_lax_oneway):
        f = jfk_lax_oneway[0]
        # One-way responses always carry a per-row price — None here
        # would indicate a regression in the price-head parsing.
        assert f.price is not None and f.price > 0
        assert f.duration > 0
        assert f.stops >= 0
        assert len(f.legs) >= 1
        assert f.legs[0].departure_airport.name == "JFK"
        assert f.legs[0].arrival_airport.name == "LAX"

    def test_currency_decoded_to_usd(self, jfk_lax_oneway):
        # All flights captured with curr=USD should report USD.
        currencies = {f.currency for f in jfk_lax_oneway if f.currency}
        assert currencies == {"USD"}

    def test_currency_decoded_to_eur(self, jfk_lax_eur):
        currencies = {f.currency for f in jfk_lax_eur if f.currency}
        assert "EUR" in currencies

    def test_co2_emissions_populated_on_some_flights(self, jfk_lax_oneway):
        with_co2 = [f for f in jfk_lax_oneway if f.co2_emissions_g]
        assert len(with_co2) >= 1, "Expected CO2 g on at least one flight"

    def test_primary_airline_populated(self, jfk_lax_oneway):
        with_pa = [f for f in jfk_lax_oneway if f.primary_airline]
        assert with_pa, "Expected primary_airline on at least one flight"

    def test_aircraft_populated_on_some_legs(self, jfk_lax_oneway):
        with_ac = [leg for f in jfk_lax_oneway for leg in f.legs if leg.aircraft]
        assert with_ac, "Expected aircraft set on at least one leg"


class TestSnapshotFilterEnforcement:
    """The captured filter responses should reflect the filter applied."""

    _ONEWORLD = {
        "AA",
        "AS",
        "AT",
        "AY",
        "BA",
        "CX",
        "FJ",
        "IB",
        "JL",
        "MH",
        "QF",
        "QR",
        "RJ",
        "S7",
        "UL",
        "DE",  # Condor is a Oneworld-connect partner
    }

    def test_oneworld_filter_only_oneworld(self, jfk_fra_oneworld):
        carriers = {f.legs[0].airline.name.lstrip("_") for f in jfk_fra_oneworld}
        # Ignore "multi" pseudo-code for codeshares.
        carriers.discard("multi")
        outside = carriers - self._ONEWORLD
        assert not outside, f"Non-Oneworld carriers found in fixture: {outside}"

    def test_exclude_dl_drops_delta(self, jfk_lax_exclude_dl):
        carriers = {f.legs[0].airline.name.lstrip("_") for f in jfk_lax_exclude_dl}
        assert "DL" not in carriers

    def test_min_layover_120_minutes_all_satisfy(self, buf_ath_min_layover):
        # Every layover on multi-leg flights must be ≥ 120 minutes.
        for f in buf_ath_min_layover:
            if not f.layovers:
                continue
            for lo in f.layovers:
                assert lo.duration >= 120, f"Found {lo.duration}-min layover at {lo.airport.name}"


class TestSnapshotLayoversAndOvernight:
    """Layover derivation works correctly across a multi-stop fixture."""

    def test_layovers_present_when_multi_leg(self, buf_ath_min_layover):
        # Most BUF→ATH results are 2-stop and should have layovers.
        with_layovers = [f for f in buf_ath_min_layover if f.layovers]
        assert with_layovers, "Expected layovers on at least one BUF→ATH flight"

    def test_layover_airport_matches_inter_leg_airports(self, buf_ath_min_layover):
        for f in buf_ath_min_layover:
            if not f.layovers:
                continue
            for i, lo in enumerate(f.layovers):
                assert lo.airport == f.legs[i].arrival_airport, (
                    "Layover airport should match the previous leg's arrival"
                )

    def test_overnight_flag_consistent_with_dates(self, buf_ath_min_layover):
        # When the flag is True, the leg actually crosses midnight.
        for f in buf_ath_min_layover:
            for leg in f.legs:
                if leg.overnight:
                    # Arrival on a later date than departure (or arrival
                    # time < departure time within the same day).
                    assert (
                        leg.arrival_datetime.date() > leg.departure_datetime.date()
                        or leg.arrival_datetime < leg.departure_datetime
                    )

    def test_layover_city_populated_when_detail_block_present(self, buf_ath_min_layover):
        # Google's detail[13] block carries layover city names; at least one
        # multi-stop flight should have city populated on a layover.
        for f in buf_ath_min_layover:
            if not f.layovers:
                continue
            if any(lo.city for lo in f.layovers):
                return
        pytest.fail("Expected at least one layover with a city name from detail[13]")


class TestPremiumRoundTripPriceless:
    """Regression coverage for issue #165.

    The fixture captures a Google response where every outbound row has
    an empty price head — that is, ``row[1] == [[], "<token>"]``. The
    parser must treat this as "Google declined to surface a price for
    this row" rather than "malformed row", because the per-row booking
    token at ``row[8]`` is still valid and resolves to real fares via
    ``GetBookingResults``.
    """

    def test_all_rows_have_none_price(self, lax_lhr_rt_biz_2a1c):
        """Uniformly empty price heads land as ``price=None`` on every row."""
        assert lax_lhr_rt_biz_2a1c
        assert all(f.price is None for f in lax_lhr_rt_biz_2a1c), (
            "Expected every premium-RT row to surface with price=None; "
            "got prices: " + str([f.price for f in lax_lhr_rt_biz_2a1c])
        )
        # The convenience property mirrors the None check exactly.
        assert all(f.price_unknown for f in lax_lhr_rt_biz_2a1c)

    def test_currency_decode_does_not_raise(self, lax_lhr_rt_biz_2a1c):
        """Empty-head price blocks may omit the currency hint without raising."""
        for f in lax_lhr_rt_biz_2a1c:
            # ``str | None`` — accept either; just confirm no exception.
            assert f.currency is None or isinstance(f.currency, str)

    def test_booking_tokens_populated(self, lax_lhr_rt_biz_2a1c):
        """Per-row booking_token (row[8]) is still set on priceless rows."""
        with_token = [f for f in lax_lhr_rt_biz_2a1c if f.booking_token]
        assert with_token == lax_lhr_rt_biz_2a1c, (
            "Every priceless premium-RT row must carry a booking_token"
        )

    def test_routing_info_populated(self, lax_lhr_rt_biz_2a1c):
        """Routing fields (legs, airports) populate even when price is unknown."""
        for f in lax_lhr_rt_biz_2a1c:
            assert len(f.legs) >= 1
            assert f.duration > 0
            assert f.legs[0].departure_airport.name == "LAX"
            assert f.legs[-1].arrival_airport.name == "LHR"
